fun main() {
    val day:Int = 4
    when(day){                       //Like "switch" in JAVA
        1 -> println("Saturday")
        2 -> println("Monday")
        3 -> println ("Sunday")
        4 -> println("Tuseday")
        5 -> println("Wednsday")
        6 -> println("Thursday")
        7 -> println("Friday")
        else ->{
            println("looking forward to the weekday")
        }
    }
}